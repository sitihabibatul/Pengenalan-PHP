<?php

	// Mendefinisikan Konstanta Phi
	define("PHI", 3.14);

	// Variabel permukaan lingkaran
	$angka = 4;
	$jarijarilingkaran = 3;
	$tinggi = 3;

	// Menghitung luas permukaan lingkaran
	$luasPermukaanLingkaran = $angka * PHI * $jarijarilingkaran * $tinggi;

	// Menampilkan luas permukaan lingkaran
	echo "Luas Permukaan Lingkaran: " . $luasPermukaanLingkaran . "m2";

?> 